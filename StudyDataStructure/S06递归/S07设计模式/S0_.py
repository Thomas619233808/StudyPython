'''
面对对象设计SOLID原则：
1、开放封闭：一个软件实体应对扩展开放，对修改关闭
2、里氏替换：所有引用父类的地方必须能透明地使用其子类的对象
3、依赖倒置：高层模块不应该依赖底层模块，二者都应该依赖其抽象（接口），抽象不应该依赖细节，细节应该依赖抽象。
换言之，应该面向接口编程，而不是针对实现编程。
4、接口隔离：使用多个专门的接口，而不使用单一总接口，即客户端不应该依赖那些不需要的接口
5、单一职责：不要存在多于一个导致类变更的原因。即一个类只负责一个职责
'''

